    private void Satuan1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Satuan1KeyTyped
        char satuan = evt.getKeyChar();
        if (!(Character.isAlphabetic(satuan)
                ||(satuan==KeyEvent.VK_BACK_SPACE)
                ||(satuan==KeyEvent.VK_DELETE)
                )) {
            evt.consume();
            JOptionPane.showMessageDialog(this, "Masukkan karakter huruf");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_Satuan1KeyTyped

    private void jumlah1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jumlah1KeyTyped
     char jumlah = evt.getKeyChar();
        if (!(Character.isDigit(jumlah)
                ||(jumlah==KeyEvent.VK_BACK_SPACE)
                ||(jumlah==KeyEvent.VK_DELETE)
                )) {
            evt.consume();
            JOptionPane.showMessageDialog(this, "Harap isi angka");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jumlah1KeyTyped


